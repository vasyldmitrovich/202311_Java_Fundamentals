package com.softserve.edu01.pt;

import java.util.Scanner;

public class pt3 {
    public static void main(String[] args) {
        var sc = new Scanner(System.in);
        System.out.println("input a:");
        int a = sc.nextInt();
        System.out.println("input b:");
        int b = sc.nextInt();
        System.out.println("a+b=" + (a+b));
        System.out.println("a-b=" + (a-b));
        System.out.println("a*b=" + (a*b));
        System.out.println("a/b=" + (a/b));
    }
}
